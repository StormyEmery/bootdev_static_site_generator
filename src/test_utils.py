    def test_markdown_to_blocks(self):
        md = """
    This is **bolded** paragraph

    This is another paragraph with _italic_ text and `code` here
    This is the same paragraph on a new line

    - This is a list
    - with items
    """
        
        blocks = markdown_to_blocks(md)
        self.assertEqual(
        blocks,
        [
            "This is **bolded** paragraph",
            "This is another paragraph with _italic_ text and `code` here\nThis is the same paragraph on a new line",
            "- This is a list\n- with items",
        ],
    )
        
    def test_markdown_to_blocks_no_paragraphs(self):
        md = "This is a single line of text"
        blocks = markdown_to_blocks(md)
        self.assertEqual(
            blocks,
            ["This is a single line of text"],
        )

    def test_markdown_to_blocks_two(self):
        md = "This is a single line of text\n\nThis is another line of text"
        blocks = markdown_to_blocks(md)
        self.assertEqual(
            blocks,
            ["This is a single line of text", "This is another line of text"],
        )